public class J_Star_pattern {
    public static void main(String args[]) {
        //NESTED LOOPS (Loop inside loop)
            //Nested IF ELSE
            
        //WAP to print a star pattern
            for (int i=1 ; i<=4 ; i++){      // i for Line
                for(int j=1 ; j<=i ; j++){   // j for star
                    System.out.print("*");

            }System.out.println();
        }    
    }     
}   


